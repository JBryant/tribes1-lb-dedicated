$MinWaitToRepeatEvent = 60 * 15; //15min wait, After this long the event can randomly happen
$MaxWaitToRepeatEvent = (60 * 60) * 5; //5hours Max wait, if reached next spawn will force event if Master bot is dead.

function SetRandomBotEvents(%aiName) {

	%time = getIntegerTime(true) >> 5;
	if($AIEvents::[%aiName, time] == "") {
		$AIEvents::[%aiName, time] = %time;
		return false; //First time bot spawned, no events
	}

	if($AIEvents::[%aiName, Alive])
		return false;

	if(%time - $AIEvents::[%aiName, time] >= $MinWaitToRepeatEvent) { //Min wait before next MasterBot can be randomly spawned
		if(floor(getRandom() * 150) == 1) {
			$AIEvents::[%aiName, time] = %time;
			return true;
		}
	}
	if(%time - $AIEvents::[%aiName, time] >= $MaxWaitToRepeatEvent) {
		$AIEvents::[%aiName, time] = %time;
		return true;
	}

	return false;
}

